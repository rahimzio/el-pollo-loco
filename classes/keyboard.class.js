/**
 * Represents the keyboard input in the game.
 * @class
 */
class Keyboard {
    /**
     * Indicates whether the jump key is pressed.
     * @type {boolean}
     */
    jump = false;

    /**
     * Indicates whether the left arrow key is pressed.
     * @type {boolean}
     */
    left = false;

    /**
     * Indicates whether the right arrow key is pressed.
     * @type {boolean}
     */
    right = false;

    /**
     * Indicates whether the down arrow key is pressed.
     * @type {boolean}
     */
    down = false;

    /**
     * Indicates whether the space key is pressed.
     * @type {boolean}
     */
    space = false;

    /**
     * Indicates whether the throw key is pressed.
     * @type {boolean}
     */
    throw = false;
}